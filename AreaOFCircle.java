public class AreaOFCircle {
    public static void main(String[] args) {
        double rad = 10;
        double pi = 3.141592653589793238;
        double area = pi*rad*rad;
        double circum = 2*pi*rad;
        System.out.println("circum "+circum);
        System.out.println("area: "+area);
    }
}
